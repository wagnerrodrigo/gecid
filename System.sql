CREATE TABLE TPROJETO (
   CODIGO_PROJETO INT(5) NOT NULL,
   TITULO VARCHAR(100) NOT NULL,
   INSTITUICAO VARCHAR(150),
   DURACAO VARCHAR(5),
   CODIGO_AREA INT(5),
   RG_PESQUISADOR VARCHAR(15),
   CONSTRAINT PK_TPROJETO PRIMARY KEY(CODIGO_PROJETO),
   CONSTRAINT FK_CODIGO_AREA FOREIGN KEY(CODIGO_AREA) REFERENCES TAREA,
   CONSTRAINT FK_RG_PESQUISADOR FOREIGN KEY(RG_PESQUISADOR) REFERENCES TPESQUISADOR(RG_PESQUISADOR) 
);


